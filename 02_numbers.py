# Arithmetic Operators

print(3 + 4)
print(3 - 4)
print(3 * 4)
print(3 / 4)
print(3 % 4)
print(10 // 3)
print(3 ** 3)

#Comparatives Operators
print(3 < 4)
print(3 > 4)
print(3 >= 4)
print(3 <= 4)
print(3 == 4)
print(3 != 4)

print ("aaaa" <= "aaaa") #Alphabetical ordering by ASCII

# Logical Operators
print(3 > 4 and "Hola" >"Python")
print(3 > 4 or "Hola" >"Python")
print(3 > 4 or ("Hola" > "Python" and 4 == 4))
print(not 3 > 4)

#Extra Operators
print("1 is 1", 1 is 1)
print("1 is not 2", 1 is not 2)
print("A in Asabeneh", "A" in "Assabeneh")
print("B in Asabeneh", "B" in "Asabeneh")
print("coding in coding for a all", "coding" in "coding for a all")
print("a in an:", "a" in "an")
print("4 is 2 ** 2", 4 is 2 ** 2)

